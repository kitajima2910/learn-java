    private void userTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTableMouseClicked
        int rowIndex = userTable.getSelectedRow(); 
        btnChange.setEnabled(true);
        String user = String.valueOf(userTable.getValueAt(rowIndex, 0));
        String per = String.valueOf(userTable.getValueAt(rowIndex, 1));
        txtPass.setText("");
        txtUser.setText(user);
        if (per.equals("Admin"))
            radAd.setSelected(true);
        else if (per.equals("User"))
            radUser.setSelected(true);
    }//GEN-LAST:event_userTableMouseClicked

    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
        try {
            char[] arrPass = txtPass.getPassword();
            String pass = new String(arrPass), user = txtUser.getText();
            if (pass.length()>3){
                sql = "UPDATE Account SET Password='"+pass+"' WHERE UserName='"+user+"'";
                stmt.executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "Update password successfully!");
            }
            else
                JOptionPane.showMessageDialog(this, "Password must be greater than 3");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnChangeActionPerformed

