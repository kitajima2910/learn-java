    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String user = txtUser.getText();
        char[] arrPass = txtPass.getPassword();
        String role = null, pass = new String(arrPass);
        if (radAd.isSelected())
            role = "Admin";
        else if (radUser.isSelected())
            role = "User";
        try {
            sql = "select * from Account";
            rs = stmt.executeQuery(sql);
            boolean notfound = true;
            if (user.length()<3 || pass.length()<3){
                JOptionPane.showMessageDialog(this, "Username & password must be greater than 3");
                txtUser.grabFocus();
                return;
            }
            while (rs.next()) {
                if ( user.equalsIgnoreCase(rs.getString("UserName"))){
                    if ( pass.equals(rs.getString("Password")) && pass.length() > 3 ){
                        if ( role!=null && role.equals(rs.getString("Permission")) ){
                            if (role.equals("Admin")){
                                adminPermission();
                            } else if (role.equals("User")){
                                userPermission(user);
                            }
                            btnLogin.setEnabled(false);
                            btnLogout.setEnabled(true);
                            txtUser.setEnabled(false);
                            return;
                        }
                    }
                }  
            }
            if (notfound){
                JOptionPane.showMessageDialog(this, "Please check your username, password or permission");
                txtUser.grabFocus();
                return;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

